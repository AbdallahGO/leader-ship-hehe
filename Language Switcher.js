  // ── COUNTDOWN ──
  function updateCountdown() {
    const target = new Date('2026-08-12T09:00:00').getTime();
    const now = Date.now();
    const diff = target - now;
    if (diff <= 0) return;
    const days = Math.floor(diff / 86400000);
    const hrs  = Math.floor((diff % 86400000) / 3600000);
    const min  = Math.floor((diff % 3600000) / 60000);
    const sec  = Math.floor((diff % 60000) / 1000);
    document.getElementById('cd-days').textContent = String(days).padStart(3,'0');
    document.getElementById('cd-hrs').textContent  = String(hrs).padStart(2,'0');
    document.getElementById('cd-min').textContent  = String(min).padStart(2,'0');
    document.getElementById('cd-sec').textContent  = String(sec).padStart(2,'0');
  }
  updateCountdown();
  setInterval(updateCountdown, 1000);

  // ── INTERSECTION OBSERVER (reveal + stats) ──
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.12 });

  document.querySelectorAll('.reveal, .stat-item').forEach(el => observer.observe(el));

  // ── STAGGER REVEALS ──
  const staggerObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        const children = entry.target.querySelectorAll('.speaker-card, .exp-feature, .sponsor-item');
        children.forEach((child, i) => {
          setTimeout(() => child.classList.add('visible'), i * 80);
        });
        staggerObserver.unobserve(entry.target);
      }
    });
  }, { threshold: 0.05 });

  document.querySelectorAll('.speakers-grid, .exp-features, .sponsors-grid').forEach(el => {
    staggerObserver.observe(el);
  });

  // Stats stagger
  const statObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        const stats = entry.target.querySelectorAll('.stat-item');
        stats.forEach((stat, i) => {
          setTimeout(() => stat.classList.add('visible'), i * 120);
        });
        statObserver.unobserve(entry.target);
      }
    });
  }, { threshold: 0.3 });
  const statsGrid = document.querySelector('.stats-grid');
  if (statsGrid) statObserver.observe(statsGrid);

